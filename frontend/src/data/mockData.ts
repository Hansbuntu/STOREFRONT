export function getSellerById(id: number): Seller | undefined {
  return sellers.find((s) => s.id === id);
}

export function getListingsBySeller(sellerId: number): Listing[] {
  return listings.filter((l) => l.sellerId === sellerId);
}

export function getFeedbackBySeller(sellerId: number): Feedback[] {
  return feedback.filter((f) => f.sellerId === sellerId);
}

export function getFeedbackBreakdown(sellerId: number) {
  const sellerFeedback = getFeedbackBySeller(sellerId);
  const breakdown = { 5: 0, 4: 0, 3: 0, 2: 0, 1: 0 };
  sellerFeedback.forEach((f) => {
    breakdown[f.stars as keyof typeof breakdown]++;
  });
  return breakdown;
}
